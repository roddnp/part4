
public class TriangleAndSquareNums 
{
	public static void main ( String[] args)
	{
		for (int i = 1 ; i < 20 ; i++) {  
	          
	        int triangle = i*(i+1)/2;  
	        
	        int square = i*i;
	        
	        System.out.println( "n="+i+" triangle number="+triangle );  
	        System.out.println( "n="+i+" square number="+square ); 
	              
	    }  
	}

}
